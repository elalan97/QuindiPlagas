    private void cbColumnaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbColumnaItemStateChanged
        // TODO add your handling code here:

        String datoSeleccionado;

        if (evt.getStateChange() == ItemEvent.SELECTED) {

            datoSeleccionado = (String) evt.getItem();

            if (datoSeleccionado.equals("Fecha Vencimiento")) {

                jcFecha.setEnabled(true);
                txtDato.setEnabled(false);

            } else {

                txtDato.setEnabled(true);
                jcFecha.setEnabled(false);

            }
        }
    }//GEN-LAST:event_cbColumnaItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String columna, dato, fecha;

        columna = (String) cbColumna.getSelectedItem();

        if (columna.equals("Seleccione")) {
            JOptionPane.showMessageDialog(null, "ingrese un dato o una fecha");
        } else {

            try {

                if (columna.equals("Fecha Vencimiento")) {

                    fecha = ((JTextField) jcFecha.getDateEditor().getUiComponent()).getText();

                    ArrayList<Inventario> lista = ctlInventario.listaInventarioFiltro(columna, fecha);

                    DefaultTableModel modelo = (DefaultTableModel) tbInventario.getModel();
                    modelo.setRowCount(0);

                    for (Inventario inventario : lista) {

                        modelo.addRow(new Object[]{inventario.getNroLote(),
                            inventario.getNombreProducto(), inventario.getFechaVencimiento(),
                            inventario.getCantidad(), inventario.getValor()});

                    }

                } else {

                    dato = txtDato.getText();

                    ArrayList<Inventario> lista = ctlInventario.listaInventarioFiltro(columna, dato);

                    DefaultTableModel modelo = (DefaultTableModel) tbInventario.getModel();
                    modelo.setRowCount(0);

                    for (Inventario inventario : lista) {

                        modelo.addRow(new Object[]{inventario.getNroLote(),
                            inventario.getNombreProducto(), inventario.getFechaVencimiento(),
                            inventario.getCantidad(), inventario.getValor()});

                    }

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


