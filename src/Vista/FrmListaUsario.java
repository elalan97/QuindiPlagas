    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:

        String dato, columna;

        dato = txtFiltro.getText();
        columna = (String) cbFiltro.getSelectedItem();

        if (dato.isEmpty() || columna.equals("Seleccione")) {

            JOptionPane.showMessageDialog(null, "por favor seleccione una columna y digite el dato");

        } else {

            try {

                ArrayList<Usuario> listausuario = ctlUsuario.filtarUsuario(columna, dato);

                DefaultTableModel modelo = (DefaultTableModel) tbListaUsuario.getModel();
                modelo.setRowCount(0);

                for (Usuario usuario1 : listausuario) {

                    modelo.addRow(new Object[]{usuario1.getNombre_Usuario(), usuario1.getPw(),
                        usuario1.getTipo_Usuario(), usuario1.getEstado(), usuario1.getNombre(), usuario1.getApellido()});

                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }

        }
    }//GEN-LAST:event_btnFiltrarActionPerformed


