    public void cargarCombo() {
        cbFiltro.removeAllItems();
        cbFiltro.addItem("Seleccione");
        cbFiltro.addItem("nombre_Usuario");
        cbFiltro.addItem("tipo_Usuario");
        cbFiltro.addItem("estado");
        cbFiltro.addItem("nombre");
        cbFiltro.addItem("apellido");
    }

    public void listar(boolean estado) {

        if (estado == true) {

            ArrayList<Usuario> usu = ctlUsuario.listarUsuario();

            DefaultTableModel modelo = (DefaultTableModel) tbListaUsuario.getModel();
            modelo.setRowCount(0);

            for (Usuario usuario1 : usu) {

                modelo.addRow(new Object[]{usuario1.getNombre_Usuario(), usuario1.getPw(),
                    usuario1.getTipo_Usuario(), usuario1.getEstado(), usuario1.getNombre(), usuario1.getApellido()});

            }
        }

    }

    public void botonesInabilitar(boolean estado) {

        btnFiltrar.setEnabled(estado);

    }

    public Boolean verificar(String usuario) {

        Usuario usu = ctlUsuario.tipoUsuario(usuario);

        if (usu.getTipo_Usuario().equals("Empleado")) {

            JOptionPane.showMessageDialog(null, "usted no es administrador");
            return false;
        }

        return true;
    }

