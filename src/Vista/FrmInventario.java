    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        int cantidad, valor, stock;
        String nombreProducto, nroLote, fechaVencimiento, valorCadena, descripcion;

        nombreProducto = txtNombreProducto.getText();
        nroLote = txtNroLote.getText();
        fechaVencimiento = ((JTextField) jcFechaVencimiento.getDateEditor().getUiComponent()).getText();
        valorCadena = txtValor.getText();
        cantidad = Integer.parseInt(txtCantidad.getText());
        descripcion = txtDescripcion.getText();
        stock = Integer.parseInt(txtStock.getText());

        if (nombreProducto.isEmpty() || nroLote.isEmpty() || fechaVencimiento.isEmpty()
                || cantidad == 0 || valorCadena.isEmpty() || descripcion.isEmpty() || stock == 0) {

            JOptionPane.showMessageDialog(null, "por favor llene los campos");

        } else {

            try {

                String num = valorCadena.replace(".", "");
                valor = Integer.parseInt(num);

                Inventario inventario = new Inventario(0, cantidad, valor,
                        nombreProducto, nroLote, fechaVencimiento);

                ctlInventario.agregarStock(inventario, stock, descripcion);
                JOptionPane.showMessageDialog(null, "se ha Ingresado correctamente");
                vaciarCampos();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

