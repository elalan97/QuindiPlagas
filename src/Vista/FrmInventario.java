    private void txtValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyReleased
        // TODO add your handling code here:

        Miles op = new Miles();

        if (!txtValor.getText().isEmpty()) {

            double numero;
            String num = txtValor.getText();
            String numeracion = num.replace(".", "");
            numero = Double.parseDouble(numeracion);
            txtValor.setText(op.separarMiles(numero));

        } else {
            txtValor.setText("");
        }
    }//GEN-LAST:event_txtValorKeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        Date fecha;
        int valor1;
        String nroLote, fecha1, valor;

        nroLote = txtNroLote.getText();

        if (nroLote.isEmpty()) {

            JOptionPane.showMessageDialog(null, "por favor busque por nro de lote");

        } else {
            try {

                Miles miles = new Miles();
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");

                Inventario inventario = ctlInventario.buscarInventario(nroLote);

                txtCantidad.setText(inventario.getCantidad() + "");
                txtNombreProducto.setText(inventario.getNombreProducto());
                txtNroLote.setText(inventario.getNroLote());

                valor1 = inventario.getValor();
                valor = miles.separarMiles(valor1);
                txtValor.setText(valor);

                fecha1 = inventario.getFechaVencimiento();
                fecha = format.parse(fecha1);
                jcFechaVencimiento.setDate(fecha);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

